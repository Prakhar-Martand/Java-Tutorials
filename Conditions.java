public class Conditions {
    public static void main(String[] args) {
        /*
         * Syntax of if condtion:
         * 
         * if(condition){statements;}
         * else{statements;}
         * 
         */

        int salary = 25000;
        if(salary > 10000)
            System.out.println("Salary is greater than 10000");
        else
            System.out.println("Salary is less than 10000");
    
}
}
